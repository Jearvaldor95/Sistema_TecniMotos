public class DeudasDatos {
    
    public int IdDeuda;
    public int IdCliente;
    public String FechaDeuda;
    public double ValorDeuda;

    public DeudasDatos() {
    }

    public int getIdDeuda() {
        return IdDeuda;
    }

    public void setIdDeuda(int IdDeuda) {
        this.IdDeuda = IdDeuda;
    }

    public int getIdCliente() {
        return IdCliente;
    }

    public void setIdCliente(int IdCliente) {
        this.IdCliente = IdCliente;
    }

    public String getFechaDeuda() {
        return FechaDeuda;
    }

    public void setFechaDeuda(String FechaDeuda) {
        this.FechaDeuda = FechaDeuda;
    }

    public double getValorDeuda() {
        return ValorDeuda;
    }

    public void setValorDeuda(double ValorDeuda) {
        this.ValorDeuda = ValorDeuda;
    }
    
    
    
}
